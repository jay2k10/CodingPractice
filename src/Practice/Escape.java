package Practice;

public class Escape {
    public static void main(String[] args) {
        System.out.println("Happy 'KGCoding'......");
        System.out.println("Happy \"KGCoding\"......");   //escape double quotes.
        System.out.println("Happy \\KGCoding\\......");   //To show backslash
        System.out.println("Happy \nKGCoding......");   //To show in the next line
        System.out.println("Happy \tKGCoding......");   //Add tab in both words
        System.out.println("Happy \bKGCoding......");   //Remove one place or cursor move one place before
    }
}
